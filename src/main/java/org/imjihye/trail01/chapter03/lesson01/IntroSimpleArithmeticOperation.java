package org.imjihye.trail01.chapter03.lesson01;

import java.util.*;

/**
 * @see <a href="https://www.codetree.ai/ko/trails/complete/curated-cards/intro-simple-arithmetic-operation/description">LINK</a>
 */
public class IntroSimpleArithmeticOperation {

  public static void main(String[] args) {

    Scanner sc = new Scanner(System.in);
    int a = sc.nextInt();
    int b = sc.nextInt();

    System.out.println(a + b);
    System.out.println(a - b);
    System.out.println(a / b);
    System.out.println(a % b);
  }
}
